/**
 * SHIFARAT_WORDS - Record format for backward compatibility with shifarat-logic.ts
 */
export const SHIFARAT_WORDS: Record<string, WordEntry[]> = {};
for (const cat of ALL_CATEGORIES) {
  SHIFARAT_WORDS[cat.id] = cat.words;
}

/**
 * Get all words from selected categories, shuffled
 */
export function getWordsForCategories(categoryIds: string[]): WordEntry[] {
  const allWords: WordEntry[] = [];
  for (const cat of ALL_CATEGORIES) {
    if (categoryIds.includes(cat.id)) {
      allWords.push(...cat.words);
    }
  }
  // Fisher-Yates shuffle
  for (let i = allWords.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [allWords[i], allWords[j]] = [allWords[j], allWords[i]];
  }
  return allWords;
}

/**
 * Get a random word that hasn't been used yet
 */
export function getRandomWord(categoryIds: string[], usedWords: Set<string>): WordEntry | null {
  const available = getWordsForCategories(categoryIds).filter(
    (entry) => !usedWords.has(entry.w)
  );
  if (available.length === 0) return null;
  return available[Math.floor(Math.random() * available.length)];
}
