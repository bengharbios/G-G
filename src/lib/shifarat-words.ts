/**
 * Pick 25 random unique words for a Codenames game board.
 * Optionally excludes previously used words.
 */
export function getBoardWords(exclude?: string[]): CodenameWord[] {
  const excludeSet = new Set(exclude || []);
  const available = WORDS.filter((word) => !excludeSet.has(word.w));

  // Fisher-Yates shuffle
  const shuffled = [...available];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled.slice(0, 25);
}

